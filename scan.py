def _d(s):
    return ''.join(chr(int(x)) for x in s.split(',')).encode()

def _ds(s):
    return base64.b64decode(_d(s)).decode()

def _xd(d, k):
    r = bytearray()
    kl = len(k)
    for i, b in enumerate(d):
        r.append(b ^ k[i % kl])
    return bytes(r)

def _rc(d, cs=48):
    c = [d[i:i+cs] for i in range(0, len(d), cs)]
    r = [ch[::-1] for ch in c]
    return b''.join(r)

def _ad(d, k, i):
    c = Cipher(algorithms.AES(k), modes.CFB(i), backend=default_backend())
    dc = c.decryptor()
    return dc.update(d) + dc.finalize()

